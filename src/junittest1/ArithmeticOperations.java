package junittest1;

public class ArithmeticOperations {
	public void add() {
		int a=10;
		int b=20;
		System.out.println(a+b);
	}
	public void sub() {
		int a=10;
		int b=20;
		System.out.println(a-b);
	}
	public void mul() {
		int a=10;
		int b=20;
		System.out.println(a*b);
	}
	public void div() {
		int a=20;
		int b=10;
		System.out.println(a/b);
	}
	
	
}
